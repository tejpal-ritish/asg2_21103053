#Q3. wap to perform bitwise operations on 2 numbers

a=56
b=10

print('The two numbers input are',a,'and',b)
print('The result of bitwise "And" operation is ->',a&b)
print('The result of bitwise "Or" operation is ->',a|b)
print('The result of bitwise "Exclusive Or" operation is ->',a^b)
print('The result of "Left Shift" operation by 2 bits on both the numbers is ->',a<<2,'and',b<<2)
print('The result of "Right Shift" operation on 56 by 2 bits is ->',a>>2)
print('The result of "Right Shift" operation on 10 by 4 bits is ->',b>>4)

